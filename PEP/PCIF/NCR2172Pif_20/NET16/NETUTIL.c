/////////////////////////////////////////////////////////////////////////////
//
// Function:	
//
// Prototype:	SHORT		net2hs(sWord);
//
// Inputs:		SHORT		sWord;			-
//
// Outputs:		SHORT		sResult;		-
//
/////////////////////////////////////////////////////////////////////////////

SHORT net2hs(SHORT sWord)
{
	SHORT	sResult;

	sResult  = (sWord & 0x00ff) << 8;
	sResult |= (sWord & 0xff00) >> 8;

	// exit ...

	return sResult;
}

/////////////////////////////////////////////////////////////////////////////
//
// Function:	
//
// Prototype:	SHORT		hs2net(sWord);
//
// Inputs:		SHORT		sWord;			-
//
// Outputs:		SHORT		sResult;		-
//
/////////////////////////////////////////////////////////////////////////////

SHORT hs2net(SHORT sWord)
{
	// exit ...

	return net2hs(sWord);
}

/////////////////////////////////////////////////////////////////////////////
//
// Function:	
//
// Prototype:	ULONG		net2hl(ulDWord);
//
// Inputs:		ULONG		ulDWord;		-
//
// Outputs:		ULONG		ulResult;		-
//
/////////////////////////////////////////////////////////////////////////////

ULONG net2hl(ULONG ulDWord)
{
	ULONG	ulResult;

	ulResult  = net2hs((SHORT) (ulDWord & 0x0000ffff)       );
	ulResult |= net2hs((SHORT)((ulDWord & 0xffff0000) >> 16))  << 16;

	// exit ...

	return ulResult;
}

/////////////////////////////////////////////////////////////////////////////
//
// Function:	
//
// Prototype:	ULONG		hl2net(ulDWord);
//
// Inputs:		ULONG		ulDWord;		-
//
// Outputs:		ULONG		ulResult;		-
//
/////////////////////////////////////////////////////////////////////////////

ULONG hl2net(ULONG ulDWord)
{
	// exit ...

	return net2hl(ulDWord);
}


