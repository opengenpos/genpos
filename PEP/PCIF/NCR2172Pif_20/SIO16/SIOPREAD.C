SHORT SioRead(DEVICE *pDevTbl, VOID *pReadBuf, SHORT sReadLen)
{
	SHORT	sResult;

    if (! (SioData.fchStatus & SIO_OPEN))
	{
        return (SYSERR);
    }

	sResult = SioRead32(pReadBuf, sReadLen);

	// exit ...

	return (sResult);
}


