SIODATA SioData;


/*
*===========================================================================
*   Static Work Area 
*===========================================================================
*/

extern BOOL IsWindowsNT(void);
/*
*===========================================================================
*   Code Area Declarations
*===========================================================================
*/
/*
*===========================================================================
** Synopsis:    SHORT SioInit(DEVICE *pDevTbl)
*     Input:    pDevTbl  - Device Table
*
** Return:      OK - Successful
*
** Description:
*===========================================================================
*/
SHORT SioInit(DEVICE *pDevTbl)
{
	// initialize

	SioData.fchStatus      = 0;
	SioData.usReceiveTimer = 0;
	SioData.usSendTimer    = 0;
	SioData.hComm          = 0;

	// set io block of device table

    pDevTbl->dvioblk = &SioData;

	IsWindowsNT ();

	SioInit32();

    return (OK);
}


