/*
*===========================================================================
** Synopsis:    SHORT SioClose(DEVICE *pDevTbl)
*     Input:    pDevTbl  - Device Table
*
** Return:      OK     - Successful
*               SYSERR - System Error
*
** Description:
*===========================================================================
*/
SHORT SioClose(DEVICE *pDevTbl)
{
	SHORT	sResult;

    if (! (SioData.fchStatus & SIO_OPEN))
	{
        return (SYSERR);
    }

	sResult = SioClose32();

	if (sResult != SIO32_OK)
	{
		return sResult;						// exit ...
	}

	SioData.fchStatus      = 0;
	SioData.usReceiveTimer = 0;
	SioData.usSendTimer    = 0;
	SioData.hComm          = 0;

	// exit ...

	return (OK);
}


